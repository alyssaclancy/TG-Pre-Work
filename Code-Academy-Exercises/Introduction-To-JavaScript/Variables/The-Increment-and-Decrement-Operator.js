let gainedDollar = 3;
let lostDollar = 50;

gainedDollar++;
lostDollar--;

console.log(gainedDollar);
console.log(lostDollar);
/*The increment operator is ++ and decrement operator is --. They can increase or decrease the value of the variable by 1.
gainedDollar increased by 1 and lostDollar decreased by 1.
*/
